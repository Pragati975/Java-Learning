import java.lang.*;

class Pyramid {
    public static void main(String[] args) {
        System.out.println("welcome to pattern pyramid");
        System.out.println("welcome to right half pyramid");
        System.out.println("*\n* *\n* * *\n* * * *\n* * * * *\n");
        System.out.println("welcome to reverse right half pyramid");
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*\n");
        System.out.println("welcome to left half pyramid");
        System.out.println("        *\n      * *\n    * * *\n  * * * *\n* * * * *\n");
    }
    
}
